/* Return an approximation of sqrt(2^64*n), with 2^62 <= n < 2^64,
   and error < 1 ulp (in unknown direction).
   We use a Taylor polynomial of degree 7.
   The maximal mathematical error is < 0.271e-19 (for n>>56 = 102) */
static mp_limb_t
mpn_sqrtrem2_approx (mp_limb_t n)
{
  int i = (n >> 56) - 64;
  mp_limb_t x, h, l;
  const mp_limb_t *u;

  x = n & 0xffffffffffffff;
  u = U[i];
  umul_ppmm (h, l, u[7], x << 1);     /* 1 = e[7] - e[6] */
  /* the error on h is less than 1 */
  umul_ppmm (h, l, u[6] - h, x << 2); /* 2 = e[6] - e[5] */
  /* the error on h is less than 1 + 2^-6 <= 2 */
  umul_ppmm (h, l, u[5] - h, x << 1); /* 1 = e[5] - e[4] */
  /* the error on h is less than 1 + 2^-7 * 2 <= 2 */ 
  umul_ppmm (h, l, u[4] - h, x << 2); /* 2 = e[4] - e[3] */
  /* the error on h is less than 1 + 2^-6 * 2 <= 2 */
  umul_ppmm (h, l, u[3] - h, x << 1); /* 1 = e[3] - e[2] */
  /* the error on h is less than 1 + 2^-7 * 2 <= 2 */
  umul_ppmm (h, l, u[2] - h, x);
  /* the error on h is less than 1 + 2^-8 * 2 <= 2 */
  umul_ppmm (h, l, u[1] - h, x);
  /* The truncation error on h + l/2^64 is less than 2^-8 * 2 <= 2^-7.
     With the mathematical error less than 0.49991, this makes a global
     error less than 0.50773. */
  return u[0] + h + (l >> 63); /* round to nearest: error < 0.5 + 2^-7 */
}
